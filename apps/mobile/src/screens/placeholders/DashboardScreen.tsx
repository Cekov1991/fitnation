import { useEffect, useMemo, useState } from 'react'
import {
  ActivityIndicator,
  Alert,
  ScrollView,
  Text,
  TouchableOpacity,
  View,
} from 'react-native'
import { SafeAreaView } from 'react-native-safe-area-context'
import { Image } from 'expo-image'
import { LinearGradient } from 'expo-linear-gradient'
import { useNavigation } from '@react-navigation/native'
import type { NativeStackNavigationProp } from '@react-navigation/native-stack'
import {
  Calendar,
  Dumbbell,
  Play,
  RefreshCw,
  Trophy,
  Zap,
} from 'lucide-react-native'
import {
  useBrowsableRoutines,
  usePlans,
  usePrograms,
  useProfile,
  useRegeneratePlan,
  useStartSession,
  useUpdateProfile,
} from '@fit-nation/shared'
import type {
  FitnessGoal,
  ProgramResource,
  TrainingExperience,
  WorkoutTemplateResource,
} from '@fit-nation/shared'

import { useAuth } from '../../context/AuthContext'
import { useTheme } from '../../context/ThemeContext'

import { Card } from '../../components/ui/Card'
import { FilterChip } from '../../components/ui/FilterChip'
import { GradientText } from '../../components/ui/GradientText'
import { PlanTypeSwitcher, type PlanType } from '../../components/ui/PlanTypeSwitcher'
import { SectionHeader } from '../../components/ui/SectionHeader'
import { SkeletonBox } from '../../components/ui/SkeletonBox'
import { WorkoutCard } from '../../components/ui/WorkoutCard'
import { WorkoutTemplateSelector } from '../../components/ui/WorkoutTemplateSelector'

import type { AppStackParamList } from '../../navigation/types'

type Nav = NativeStackNavigationProp<AppStackParamList>

const GOAL_OPTIONS: Array<{ value: FitnessGoal; label: string }> = [
  { value: 'fat_loss', label: 'Fat Loss' },
  { value: 'muscle_gain', label: 'Muscle Gain' },
  { value: 'strength', label: 'Strength' },
  { value: 'general_fitness', label: 'General Fitness' },
]

const EXPERIENCE_OPTIONS: Array<{ value: TrainingExperience; label: string }> = [
  { value: 'beginner', label: 'Beginner' },
  { value: 'intermediate', label: 'Intermediate' },
  { value: 'advanced', label: 'Advanced' },
]

const DAYS_OPTIONS: Array<{ value: number; label: string }> = [1, 2, 3, 4, 5, 6, 7].map((d) => ({
  value: d,
  label: `${d} day${d > 1 ? 's' : ''}`,
}))

const DURATION_OPTIONS: Array<{ value: number; label: string }> = [
  { value: 30, label: '20-30 min' },
  { value: 45, label: '30-45 min' },
  { value: 60, label: '45-60 min' },
  { value: 90, label: '60-90 min' },
  { value: 120, label: '90+ min' },
]

export function DashboardScreen() {
  const { colors } = useTheme()
  const { user } = useAuth()
  const navigation = useNavigation<Nav>()
  const [activeTab, setActiveTab] = useState<PlanType>('programs')
  const [selectedTemplateId, setSelectedTemplateId] = useState<number | null>(null)
  const [isRegenerating, setIsRegenerating] = useState(false)

  const { data: programs = [], isLoading: isProgramsLoading } = usePrograms()
  const { data: plans = [], isLoading: isPlansLoading } = usePlans()
  const { data: browsableRoutines = [] } = useBrowsableRoutines()
  const { data: profileData } = useProfile()
  const regeneratePlan = useRegeneratePlan()
  const startSession = useStartSession()
  const updateProfile = useUpdateProfile()

  const profile = profileData?.profile ?? null
  const partnerName = user?.partner?.name || 'Fit Nation'
  const partnerLogo = user?.partner?.visual_identity?.logo || null

  // ─── Programs tab ───
  const activeProgram = useMemo(
    () => programs.find((p: ProgramResource) => p.is_active) || null,
    [programs],
  )
  const autoGeneratedProgram = useMemo(
    () =>
      programs.find((p: ProgramResource) => p.is_auto_generated && p.is_active) ||
      programs.find((p: ProgramResource) => p.is_auto_generated) ||
      null,
    [programs],
  )
  const displayWeekNumber = useMemo(
    () => activeProgram?.next_workout?.week_number ?? activeProgram?.current_active_week ?? 1,
    [activeProgram],
  )
  const activeWeekWorkouts = useMemo(() => {
    if (!activeProgram?.workout_templates) return []
    return activeProgram.workout_templates
      .filter((t: WorkoutTemplateResource) => t.week_number === displayWeekNumber)
      .sort(
        (a: WorkoutTemplateResource, b: WorkoutTemplateResource) =>
          a.order_index - b.order_index,
      )
  }, [activeProgram, displayWeekNumber])

  const defaultSelectedId = useMemo(() => {
    if (
      activeProgram?.next_workout &&
      activeWeekWorkouts.some(
        (w: WorkoutTemplateResource) => w.id === activeProgram.next_workout?.id,
      )
    ) {
      return activeProgram.next_workout.id
    }
    return activeWeekWorkouts.length > 0 ? activeWeekWorkouts[0].id : null
  }, [activeProgram, activeWeekWorkouts])

  useEffect(() => {
    if (defaultSelectedId !== null) setSelectedTemplateId(defaultSelectedId)
  }, [defaultSelectedId])

  const effectiveSelectedId = selectedTemplateId ?? defaultSelectedId

  const selectedWorkout = useMemo(
    () =>
      activeWeekWorkouts.find(
        (w: WorkoutTemplateResource) => w.id === effectiveSelectedId,
      ) || null,
    [activeWeekWorkouts, effectiveSelectedId],
  )

  const isPlanComplete = useMemo(
    () =>
      !!activeProgram &&
      activeProgram.next_workout === null &&
      (activeProgram.progress_percentage ?? 0) > 0,
    [activeProgram],
  )

  const inactiveNonAutoPrograms = useMemo(
    () => programs.filter((p: ProgramResource) => !p.is_active && !p.is_auto_generated),
    [programs],
  )

  // ─── Custom Plans tab ───
  const activePlan = useMemo(() => plans.find((p) => p.is_active) || null, [plans])
  const activePlanWorkouts = useMemo(() => {
    if (!activePlan?.workout_templates) return []
    return [...activePlan.workout_templates].sort((a, b) => {
      if (a.day_of_week === null && b.day_of_week === null) return 0
      if (a.day_of_week === null) return 1
      if (b.day_of_week === null) return -1
      return a.day_of_week - b.day_of_week
    })
  }, [activePlan])

  // ─── Handlers ───
  const goalOptions = GOAL_OPTIONS.map((o) => ({ label: o.label, value: o.value }))
  const experienceOptions = EXPERIENCE_OPTIONS.map((o) => ({ label: o.label, value: o.value }))

  const goalLabel =
    GOAL_OPTIONS.find((o) => o.value === profile?.fitness_goal)?.label ?? 'Goal'
  const experienceLabel =
    EXPERIENCE_OPTIONS.find((o) => o.value === profile?.training_experience)?.label ??
    'Experience'
  const daysLabel = profile?.training_days_per_week
    ? `${profile.training_days_per_week} day${profile.training_days_per_week > 1 ? 's' : ''}`
    : 'Days/week'
  const durationLabel =
    DURATION_OPTIONS.find((o) => o.value === profile?.workout_duration_minutes)?.label ??
    'Duration'

  const isProfileComplete = Boolean(
    profile?.fitness_goal &&
      profile?.training_experience &&
      profile?.training_days_per_week &&
      profile?.workout_duration_minutes,
  )
  const hasCompletedWorkouts = (activeProgram?.progress_percentage ?? 0) > 0

  async function updateProfileField(
    key: 'fitness_goal' | 'training_experience' | 'training_days_per_week' | 'workout_duration_minutes',
    value: string | number,
  ) {
    try {
      await updateProfile.mutateAsync({ [key]: value } as any)
    } catch (e) {
      console.error('Failed to update profile', e)
    }
  }

  async function executeRegenerate() {
    setIsRegenerating(true)
    try {
      await regeneratePlan.mutateAsync()
    } catch (e) {
      console.error('Failed to regenerate plan', e)
    } finally {
      setIsRegenerating(false)
    }
  }

  const handleRefreshClick = () => {
    if (isRegenerating || !isProfileComplete) return
    if (!hasCompletedWorkouts) {
      executeRegenerate()
      return
    }
    Alert.alert(
      'Refresh personalized plan?',
      'Your current plan has completed workouts. Refreshing will create a new plan and you will start from scratch.',
      [
        { text: 'Cancel', style: 'cancel' },
        { text: 'Refresh Plan', style: 'destructive', onPress: executeRegenerate },
      ],
    )
  }

  const handleStartSelectedWorkout = async () => {
    const templateId = effectiveSelectedId ?? activeProgram?.next_workout?.id
    if (!templateId) return
    try {
      const response = await startSession.mutateAsync(templateId)
      const session = (response as any).data?.session || (response as any).data
      if (session?.id) {
        if (!session.performed_at) {
          navigation.navigate('WorkoutPreview', { sessionId: String(session.id) })
        } else {
          navigation.navigate('WorkoutSession', { sessionId: String(session.id) })
        }
      }
    } catch (e) {
      console.error('Failed to start workout', e)
    }
  }

  const handleEditWorkout = (templateId: number) => {
    navigation.navigate('EditWorkout', { templateId })
  }

  const handleStartBlankSession = async () => {
    try {
      const response = await startSession.mutateAsync(undefined)
      const session = (response as any).data?.session || (response as any).data
      if (session?.id) {
        navigation.navigate('WorkoutSession', { sessionId: String(session.id) })
      }
    } catch (e) {
      console.error('Failed to start blank session', e)
    }
  }

  const handleStartPlanWorkout = async (templateId: number) => {
    try {
      const response = await startSession.mutateAsync(templateId)
      const session = (response as any).data?.session || (response as any).data
      if (session?.id) {
        navigation.navigate('WorkoutSession', { sessionId: String(session.id) })
      }
    } catch (e) {
      console.error('Failed to start workout', e)
    }
  }

  return (
    <SafeAreaView edges={['top']} style={{ flex: 1, backgroundColor: colors.bgBase }}>
      <ScrollView
        contentContainerStyle={{ paddingHorizontal: 16, paddingBottom: 120 }}
        showsVerticalScrollIndicator={false}
      >
        {/* ─── Header ─── */}
        <View style={{ alignItems: 'center', paddingTop: 24, paddingBottom: 24 }}>
          {partnerLogo ? (
            <Image
              source={{ uri: partnerLogo }}
              style={{
                width: 64,
                height: 64,
                borderRadius: 16,
                marginBottom: 16,
              }}
              contentFit="contain"
              transition={200}
            />
          ) : (
            <LinearGradient
              colors={[colors.primary, colors.secondary]}
              start={{ x: 0, y: 0 }}
              end={{ x: 1, y: 1 }}
              style={{
                width: 64,
                height: 64,
                borderRadius: 16,
                alignItems: 'center',
                justifyContent: 'center',
                marginBottom: 16,
              }}
            >
              <Dumbbell color={colors.textButton} size={32} />
            </LinearGradient>
          )}
          <GradientText style={{ fontSize: 30, fontWeight: '800' }} numberOfLines={1}>
            {partnerName}
          </GradientText>
          <Text
            style={{
              fontSize: 14,
              marginTop: 4,
              color: colors.textSecondary,
            }}
          >
            Welcome back, {user?.name || 'Athlete'}
          </Text>
        </View>

        {/* ─── Plan-type segment ─── */}
        <PlanTypeSwitcher activeType={activeTab} onTypeChange={setActiveTab} />

        {/* ═══ Programs Tab ═══ */}
        {activeTab === 'programs' && (
          <>
            {/* Program controls — chips row */}
            {autoGeneratedProgram && (
              <ScrollView
                horizontal
                showsHorizontalScrollIndicator={false}
                contentContainerStyle={{
                  gap: 8,
                  paddingBottom: 16,
                  paddingHorizontal: 2,
                }}
              >
                {activeProgram?.is_active && (
                  <FilterChip
                    icon={Calendar}
                    onPress={() =>
                      navigation.navigate('ProgramDetail', { programId: activeProgram.id })
                    }
                  />
                )}
                <FilterChip
                  label={goalLabel}
                  options={goalOptions}
                  placeholder="Fitness goal"
                  onSelect={(v) => updateProfileField('fitness_goal', v)}
                />
                <FilterChip
                  label={experienceLabel}
                  options={experienceOptions}
                  placeholder="Training experience"
                  onSelect={(v) => updateProfileField('training_experience', v)}
                />
                <FilterChip
                  label={daysLabel}
                  options={DAYS_OPTIONS}
                  placeholder="Days per week"
                  onSelect={(v) => updateProfileField('training_days_per_week', v)}
                />
                <FilterChip
                  label={durationLabel}
                  options={DURATION_OPTIONS}
                  placeholder="Workout duration"
                  onSelect={(v) => updateProfileField('workout_duration_minutes', v)}
                />
                <FilterChip
                  icon={RefreshCw}
                  label="Refresh"
                  disabled={isRegenerating || !isProfileComplete}
                  onPress={handleRefreshClick}
                />
              </ScrollView>
            )}

            {isProgramsLoading ? (
              <>
                <SkeletonBox height={50} style={{ marginBottom: 12 }} />
                <SkeletonBox height={200} />
              </>
            ) : !activeProgram ? (
              <Card>
                <Text
                  style={{
                    textAlign: 'center',
                    fontWeight: '600',
                    color: colors.textPrimary,
                    marginBottom: 16,
                  }}
                >
                  No active program
                </Text>
                {inactiveNonAutoPrograms.map((prog) => (
                  <TouchableOpacity
                    key={prog.id}
                    onPress={() =>
                      navigation.navigate('ProgramDetail', { programId: prog.id })
                    }
                    style={{
                      flexDirection: 'row',
                      alignItems: 'center',
                      justifyContent: 'space-between',
                      paddingHorizontal: 16,
                      paddingVertical: 12,
                      borderRadius: 12,
                      marginBottom: 8,
                      backgroundColor: colors.bgElevated,
                      borderWidth: 1,
                      borderColor: colors.border,
                    }}
                  >
                    <Text
                      style={{ fontSize: 14, fontWeight: '600', color: colors.textPrimary }}
                    >
                      {prog.name}
                    </Text>
                    <Text style={{ fontSize: 12, color: colors.textSecondary }}>View →</Text>
                  </TouchableOpacity>
                ))}
                <TouchableOpacity
                  onPress={executeRegenerate}
                  disabled={isRegenerating || regeneratePlan.isPending}
                  style={{
                    flexDirection: 'row',
                    alignItems: 'center',
                    justifyContent: 'center',
                    gap: 8,
                    paddingVertical: 12,
                    borderRadius: 12,
                    marginTop: 8,
                    backgroundColor: colors.primary,
                    opacity: isRegenerating ? 0.7 : 1,
                  }}
                >
                  {isRegenerating || regeneratePlan.isPending ? (
                    <ActivityIndicator size="small" color={colors.textButton} />
                  ) : (
                    <Zap size={16} color={colors.textButton} />
                  )}
                  <Text style={{ fontWeight: '600', color: colors.textButton }}>
                    Generate personalized plan
                  </Text>
                </TouchableOpacity>
              </Card>
            ) : isPlanComplete ? (
              <Card>
                <View style={{ alignItems: 'center', marginBottom: 16 }}>
                  <View
                    style={{
                      width: 64,
                      height: 64,
                      borderRadius: 32,
                      alignItems: 'center',
                      justifyContent: 'center',
                      backgroundColor: colors.bgElevated,
                    }}
                  >
                    <Trophy size={32} color={colors.primary} strokeWidth={1.75} />
                  </View>
                </View>
                <Text
                  style={{
                    fontSize: 20,
                    fontWeight: '700',
                    textAlign: 'center',
                    marginBottom: 8,
                    color: colors.textPrimary,
                  }}
                >
                  Plan complete!
                </Text>
                <Text
                  style={{
                    fontSize: 14,
                    textAlign: 'center',
                    marginBottom: 24,
                    color: colors.textSecondary,
                  }}
                >
                  You finished all {activeProgram.duration_weeks} weeks of{' '}
                  <Text style={{ fontWeight: '600', color: colors.textPrimary }}>
                    {activeProgram.name}
                  </Text>
                  .
                </Text>
                {activeProgram.is_auto_generated && (
                  <TouchableOpacity
                    onPress={executeRegenerate}
                    disabled={isRegenerating}
                    style={{
                      flexDirection: 'row',
                      alignItems: 'center',
                      justifyContent: 'center',
                      gap: 8,
                      paddingVertical: 12,
                      borderRadius: 12,
                      backgroundColor: colors.primary,
                      opacity: isRegenerating ? 0.7 : 1,
                    }}
                  >
                    {isRegenerating ? (
                      <ActivityIndicator size="small" color={colors.textButton} />
                    ) : (
                      <Zap size={16} color={colors.textButton} />
                    )}
                    <Text style={{ fontWeight: '600', color: colors.textButton }}>
                      Generate new plan
                    </Text>
                  </TouchableOpacity>
                )}
              </Card>
            ) : (
              <>
                {/* Day selector */}
                {activeWeekWorkouts.length > 0 && (
                  <View style={{ marginBottom: 12 }}>
                    <WorkoutTemplateSelector
                      templates={activeWeekWorkouts}
                      selectedTemplateId={effectiveSelectedId}
                      onTemplateSelect={setSelectedTemplateId}
                      nextWorkout={activeProgram.next_workout ?? null}
                    />
                  </View>
                )}

                {/* Week indicator */}
                <Text
                  style={{
                    fontSize: 12,
                    fontWeight: '700',
                    letterSpacing: 1.2,
                    textAlign: 'center',
                    marginBottom: 16,
                    color: colors.primary,
                    textTransform: 'uppercase',
                  }}
                >
                  Week {displayWeekNumber}/{activeProgram.duration_weeks ?? '?'}
                  {activeProgram.name ? ` — ${activeProgram.name}` : ''}
                </Text>

                {/* Workout card */}
                {selectedWorkout ? (
                  <WorkoutCard
                    template={selectedWorkout}
                    title={selectedWorkout.name || 'WORKOUT'}
                    showStartButton
                    startButtonText={effectiveSelectedId ? 'START WORKOUT' : 'NO WORKOUTS AVAILABLE'}
                    startButtonDisabled={!effectiveSelectedId}
                    startButtonLoading={startSession.isPending}
                    onStartNextWorkout={handleStartSelectedWorkout}
                    onExerciseClick={(name) =>
                      navigation.navigate('ExerciseDetail', { exerciseName: name })
                    }
                    onEditWorkout={handleEditWorkout}
                  />
                ) : (
                  <Card>
                    <Text style={{ textAlign: 'center', color: colors.textSecondary }}>
                      No workouts available for this week
                    </Text>
                  </Card>
                )}
              </>
            )}
          </>
        )}

        {/* ═══ Custom Plans Tab ═══ */}
        {activeTab === 'customPlans' && (
          <>
            {/* AI generator */}
            <Card>
              <Text
                style={{
                  fontSize: 16,
                  fontWeight: '700',
                  marginBottom: 4,
                  color: colors.primary,
                }}
              >
                AI Workout Generator
              </Text>
              <Text style={{ fontSize: 14, marginBottom: 16, color: colors.textSecondary }}>
                Generate a personalized workout based on your goals
              </Text>
              <TouchableOpacity
                onPress={() => navigation.navigate('GenerateWorkout')}
                style={{
                  paddingVertical: 12,
                  borderRadius: 12,
                  alignItems: 'center',
                  backgroundColor: colors.primary,
                }}
              >
                <Text style={{ fontWeight: '700', color: colors.textButton }}>
                  Generate Workout
                </Text>
              </TouchableOpacity>
            </Card>

            {/* Recommended routines */}
            {browsableRoutines.length > 0 && (
              <>
                <SectionHeader title="Recommended Workouts" />
                <ScrollView
                  horizontal
                  showsHorizontalScrollIndicator={false}
                  contentContainerStyle={{ gap: 12, paddingBottom: 8 }}
                >
                  {browsableRoutines.map((routine) => (
                    <TouchableOpacity
                      key={routine.id}
                      onPress={() =>
                        navigation.navigate('RoutineDetail', { routineId: routine.id })
                      }
                      style={{
                        width: 176,
                        borderRadius: 16,
                        padding: 16,
                        backgroundColor: colors.bgSurface,
                        borderWidth: 1,
                        borderColor: colors.borderSubtle,
                      }}
                    >
                      <Text
                        numberOfLines={1}
                        style={{
                          fontSize: 14,
                          fontWeight: '700',
                          marginBottom: 4,
                          color: colors.textPrimary,
                        }}
                      >
                        {routine.name}
                      </Text>
                      <Text style={{ fontSize: 12, color: colors.textSecondary }}>
                        {routine.workout_templates?.length ?? 0} workouts
                      </Text>
                    </TouchableOpacity>
                  ))}
                </ScrollView>
                <View style={{ marginBottom: 16 }} />
              </>
            )}

            {/* Active plan workouts */}
            <SectionHeader
              title={activePlan ? activePlan.name : 'My Custom Plans'}
              action={
                activePlan
                  ? { label: 'See all', onPress: () => navigation.navigate('Tabs') }
                  : undefined
              }
            />
            {isPlansLoading ? (
              <SkeletonBox height={100} style={{ marginBottom: 16 }} />
            ) : activePlan && activePlanWorkouts.length > 0 ? (
              <ScrollView
                horizontal
                showsHorizontalScrollIndicator={false}
                contentContainerStyle={{ gap: 12, paddingBottom: 8 }}
              >
                {activePlanWorkouts.map((workout: WorkoutTemplateResource) => (
                  <View
                    key={workout.id}
                    style={{
                      width: 176,
                      borderRadius: 16,
                      padding: 16,
                      backgroundColor: colors.bgSurface,
                      borderWidth: 1,
                      borderColor: colors.borderSubtle,
                    }}
                  >
                    <Text
                      numberOfLines={1}
                      style={{
                        fontSize: 14,
                        fontWeight: '700',
                        marginBottom: 4,
                        color: colors.textPrimary,
                      }}
                    >
                      {workout.name}
                    </Text>
                    <Text
                      style={{ fontSize: 12, marginBottom: 12, color: colors.textSecondary }}
                    >
                      {workout.exercises?.length ?? 0} exercises
                    </Text>
                    <TouchableOpacity
                      onPress={() => handleStartPlanWorkout(workout.id)}
                      disabled={startSession.isPending}
                      style={{
                        paddingVertical: 8,
                        borderRadius: 10,
                        alignItems: 'center',
                        backgroundColor: colors.primary,
                      }}
                    >
                      <Text
                        style={{ fontSize: 12, fontWeight: '700', color: colors.textButton }}
                      >
                        Start
                      </Text>
                    </TouchableOpacity>
                  </View>
                ))}
              </ScrollView>
            ) : (
              <TouchableOpacity
                onPress={() => navigation.navigate('CreatePlan')}
                style={{
                  width: 160,
                  borderRadius: 16,
                  padding: 16,
                  alignItems: 'center',
                  justifyContent: 'center',
                  marginBottom: 16,
                  backgroundColor: colors.bgSurface,
                  borderWidth: 1,
                  borderStyle: 'dashed',
                  borderColor: `${colors.primary}66`,
                }}
              >
                <Text style={{ fontSize: 28, marginBottom: 4, color: colors.primary }}>
                  +
                </Text>
                <Text
                  style={{ fontSize: 12, fontWeight: '600', color: colors.textSecondary }}
                >
                  Create Plan
                </Text>
              </TouchableOpacity>
            )}
            <View style={{ marginBottom: 16 }} />

            {/* Quick start */}
            <Card>
              <Text
                style={{
                  fontSize: 16,
                  fontWeight: '700',
                  marginBottom: 4,
                  color: colors.primary,
                }}
              >
                Quick Start
              </Text>
              <Text style={{ fontSize: 14, marginBottom: 16, color: colors.textSecondary }}>
                Start a blank session without a plan
              </Text>
              <TouchableOpacity
                onPress={handleStartBlankSession}
                disabled={startSession.isPending}
                activeOpacity={0.9}
                style={{
                  height: 48,
                  borderRadius: 12,
                  overflow: 'hidden',
                  flexDirection: 'row',
                  alignItems: 'center',
                  justifyContent: 'center',
                  gap: 8,
                  opacity: startSession.isPending ? 0.7 : 1,
                }}
              >
                <LinearGradient
                  colors={[colors.secondary, colors.primary]}
                  start={{ x: 0, y: 0 }}
                  end={{ x: 1, y: 0 }}
                  style={{ position: 'absolute', top: 0, bottom: 0, left: 0, right: 0 }}
                />
                {startSession.isPending ? (
                  <ActivityIndicator color={colors.textButton} />
                ) : (
                  <>
                    <Play size={18} color={colors.textButton} fill={colors.textButton} />
                    <Text style={{ fontWeight: '700', color: colors.textButton }}>
                      Start Blank Session
                    </Text>
                  </>
                )}
              </TouchableOpacity>
            </Card>
          </>
        )}
      </ScrollView>
    </SafeAreaView>
  )
}
